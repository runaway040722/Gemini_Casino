void PlayRussianRoulette(int& money) {
    system("cls");

    // 랜덤 도구 설정
    random_device rd;
    mt19937 gen(rd());

    // [중요] globalNamePool에서 이름을 랜덤으로 하나 뽑습니다.
    uniform_int_distribution<int> nameDist(0, (int)globalNamePool.size() - 1);
    string aiName = globalNamePool[nameDist(gen)];

    uniform_int_distribution<int> coinFlip(0, 1);
    uniform_int_distribution<int> dist(0, 5);

    int bet = GetBetAmount(money);

    // 1. 선공 결정 애니메이션
    int totalSpins = 15 + (rand() % 10);
    for (int i = 0; i < totalSpins; i++) {
        ShowRotatingGun(i, aiName);
        Beep(800 + (i * 20), 50);
        Sleep(50 + (i * 5));
    }

    bool playerTurn = (coinFlip(gen) == 0);

    ShowRotatingGun(playerTurn ? 0 : 2, aiName);
    SetColor(playerTurn ? 11 : 12);
    cout << "\n    >>> " << (playerTurn ? "당신" : aiName) << "의 선공! <<<\n";
    SetColor(15);
    Sleep(2000);

    // 2. 약실 셋팅
    int bulletPos = dist(gen);
    int currentPos = dist(gen);
    int shotsFired = 0;

    bool alive = true;
    while (alive) {
        system("cls");
        SetColor(14);
        cout << "\n    [ 현재 차례: " << (playerTurn ? "플레이어" : aiName) << " ]" << endl;
        SetColor(15);

        if (playerTurn) {
            cout << "    관자놀이에 총을 겨눕니다... (Enter를 누르세요)";
            while (_getch() != 13);
        }
        else {
            cout << "    " << aiName << "(이)가 총구를 관자놀이에 밀착시킵니다...";
            Sleep(1500);
        }

        PrintArt(false, !playerTurn);
        cout << "\n    방아쇠를 당깁니다...";
        Sleep(1000);

        // 격발 체크
        if (currentPos == bulletPos) {
            PrintArt(true, !playerTurn);
            Beep(300, 600);
            Sleep(1000);

            system("cls"); // 격발 후 새 창 정리
            cout << "\n\n\n\n\n";

            if (playerTurn) {
                SetColor(12);
                cout << "    ==============================================================" << endl;
                cout << "    ||                                                          ||" << endl;
                cout << "    ||           [ ! ! ! 격 발 되 었 습 니 다 ! ! ! ]           ||" << endl;
                cout << "    ||                                                          ||" << endl;
                cout << "    ==============================================================" << endl;
                SetColor(15);
                cout << "\n              당신의 관자놀이에서 불꽃이 튀었습니다..." << endl;
                money -= bet;
                Sleep(2000);
                system("cls");
                DragToMine("당신", true);
            }
            else {
                SetColor(10);
                cout << "    ==============================================================" << endl;
                cout << "    ||                                                          ||" << endl;
                cout << "    ||       [ 승리! " << aiName << "이(가) 쓰러졌습니다. ]          ||" << endl;
                cout << "    ||                                                          ||" << endl;
                cout << "    ==============================================================" << endl;
                SetColor(15);
                cout << "\n             무거운 정적 속에 승부가 결정되었습니다." << endl;
                money += bet;
                Sleep(2000);
                system("cls");
                DragToMine(aiName, false);
            }
            alive = false;
        }
        else {
            SetColor(15);
            cout << "\n    ...딸칵!";
            Beep(1200, 100);
            Sleep(800);

            shotsFired++;
            system("cls"); // 생존 후 새 창 정리

            if (shotsFired == 5) {
                SetColor(12);
                cout << "\n\n\n\n    [!!! 약실 소진 !!!]" << endl;
                cout << "    남은 칸은 실탄뿐입니다. 약실을 새로 회전시킵니다!" << endl;
                Sleep(2000);

                for (int i = 0; i < 15; i++) {
                    ShowRotatingGun(i, aiName);
                    Beep(1000 + (i * 30), 40);
                    Sleep(40 + (i * 3));
                }

                bulletPos = dist(gen);
                currentPos = dist(gen);
                shotsFired = 0;

                system("cls");
                cout << "\n\n\n\n\n";
                SetColor(13);
                cout << "    ==============================================================" << endl;
                cout << "    ||                 [  : 약 실 회 전 완 료  ]                ||" << endl;
                cout << "    ==============================================================" << endl;
                Sleep(2000);
            }
            else {
                currentPos = (currentPos + 1) % 6;
                cout << "\n\n\n\n\n";
                SetColor(11);
                cout << "    ==============================================================" << endl;
                cout << "    ||                [  살 아 남 았 습 니 다  ]                ||" << endl;
                cout << "    ==============================================================" << endl;
                cout << "\n\n             안도의 한숨을 내쉬며 다음 차례로 넘깁니다...";
                Sleep(2000);
            }
            playerTurn = !playerTurn;
        }
    }

    SetColor(14);
    cout << "\n [아무 키나 누르면 카지노 로비로 돌아갑니다]";
    (void)_getch();
}