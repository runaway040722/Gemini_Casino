// ОЦНКХА ОЦЦЎ ЙнРќПы ЧдМі
static char FlipChar(char c) {
    switch (c) {
    case '(': return ')'; case ')': return '(';
    case '<': return '>'; case '>': return '<';
    case '[': return ']'; case ']': return '[';
    case '{': return '}'; case '}': return '{';
    case '/': return '\\'; case '\\': return '/';
    case '`': return '\''; case '\'': return '`';
    default: return c;
    }
}

